function add(a, b) {
  return a + b;
}

function divide(a, b) {
  return a / b;
}

function multiply(a, b) {
  return a * b;
}

function mod(a, b) {
  return a % b;
}

function exponent(a, b) {
  return a ** b;
}

function subt(a, b) {
  return a - b;
}

console.log(add(1, 2));
console.log(multiply(1, 2));
console.log(divide(1, 2));
console.log(mod(1, 2));
console.log(exponent(1, 2));
console.log(subt(1, 2));
